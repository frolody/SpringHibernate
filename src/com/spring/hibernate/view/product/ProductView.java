    private void refreshTable() {
        list = Main.getProductService().getAll();
        productTable.setModel(new ProductTableModel(list));
    }

    private void enableForm(boolean status) {
        txtId.setEnabled(false);
        txtNama.setEnabled(status);
        txtHarga.setEnabled(status);
    }

    private void clearForm() {
        txtNama.setText("");
        txtId.setText("");
        txtHarga.setText("");
        productTable.getSelectionModel().clearSelection();
        product = null;
    }

    private boolean validateForm() {
        return !(txtNama.getText().isEmpty() || txtHarga.getText().isEmpty());
    }

    private void loadFormToModel() {
        product.setName(txtNama.getText());
        product.setPrice(BigDecimal.valueOf(Double.valueOf(txtHarga.getText())));
    }

    private void loadModelToForm() {
        txtId.setText(String.valueOf(product.getId()));
        txtNama.setText(product.getName());
        txtHarga.setText(String.valueOf(product.getPrice()));
    }

