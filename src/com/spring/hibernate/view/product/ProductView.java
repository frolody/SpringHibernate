    private class ProductListSelectionListener implements ListSelectionListener {

        @Override
        public void valueChanged(ListSelectionEvent e) {
            if (productTable.getSelectedRow() != -1) {
                product = list.get(productTable.getSelectedRow());
                product = Main.getProductService().getProduct(product.getId());
                loadModelToForm();
                btnDelete.setEnabled(true);
                btnAdd.setEnabled(false);
                btnCancel.setEnabled(true);
                btnEdit.setEnabled(true);
                btnSave.setEnabled(false);
            }
        }

    }

    private class ProductTableModel extends AbstractTableModel {

        private List<Product> listProduct;

        public ProductTableModel(List<Product> listProduct) {
            this.listProduct = listProduct;
        }

        @Override
        public int getRowCount() {
            return listProduct.size();
        }

        @Override
        public int getColumnCount() {
            return 2;
        }

        @Override
        public Object getValueAt(int rowIndex, int columnIndex) {
            Product p = listProduct.get(rowIndex);
            switch (columnIndex) {
                case 0:
                    return p.getName();
                case 1:
                    return p.getPrice();
                default:
                    return null;
            }
        }
    }
